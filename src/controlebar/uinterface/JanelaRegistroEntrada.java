    private void cadastrarBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarBotaoActionPerformed
        // TODO add your handling code here:
        String nome = nomeCampo.getText();
        int idade = Integer.parseInt(idadeCampo.getText());
        String cpf = cpfCampo.getText();
        String genero = generoCampo.getText();
        String resposta = socioRespostaCombo.getSelectedItem().toString();
        if(resposta.equals("Sim")){
            int nroSocio = Integer.parseInt(nroSocioCampo.getText());
            Socio s = new Socio(nome, cpf, idade, genero, nroSocio);
        } else {
            Cliente c = new Cliente(nome, cpf, idade, genero);
        }
        
    }//GEN-LAST:event_cadastrarBotaoActionPerformed

