    private void socioRespostaComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_socioRespostaComboActionPerformed
        // TODO add your handling code here:

        socioRespostaCombo.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                nroSocioCampo.setEnabled(false);

                if (e.getItem().toString().equals("Sim")) {
                    nroSocioCampo.setVisible(true);
                    nroSocioCampo.setEnabled(true);
                } else {
                    nroSocioCampo.setEnabled(false);
                }

            }
        });
    }//GEN-LAST:event_socioRespostaComboActionPerformed

    
